function makeOptions(correct: string, wrong: [string, string]) {
  return [
    { id: "a", label: correct },
    { id: "b", label: wrong[0] },
    { id: "c", label: wrong[1] },
  ];
}

function makeSentenceParts(sentence: string, blankAnswer: string) {
  const index = sentence.toLowerCase().indexOf(blankAnswer.toLowerCase());

  if (index === -1) {
    return {
      before: sentence,
      answer: blankAnswer,
      after: "",
    };
  }

  return {
    before: sentence.slice(0, index).trimEnd(),
    answer: sentence.slice(index, index + blankAnswer.length),
    after: sentence.slice(index + blankAnswer.length).trimStart(),
  };
}

function makeWordChips(sentence: string) {
  return sentence.split(" ").map((label, index) => ({
    id: `w${index + 1}`,
    label,
  }));
}

function buildQuestions(spec: LessonSpec): LessonQuestion[] {
  const blank = makeSentenceParts(spec.english, spec.blankAnswer);

  return [
    {
      id: `${spec.id}-q1`,
      type: "multiple-choice",
      prompt: `Câu nào có nghĩa là: ${spec.vietnamese}`,
      options: makeOptions(spec.english, spec.wrongEnglish),
      correctOptionId: "a",
      explanation: `Câu đúng là '${spec.english}'.`,
    },
    {
      id: `${spec.id}-q2`,
      type: "fill_blank",
      prompt: "Điền từ hoặc cụm từ còn thiếu để hoàn thành câu B1.",
      sentenceParts: {
        before: blank.before,
        after: blank.after,
      },
      correctAnswer: blank.answer,
      explanation: `Câu đầy đủ là '${spec.english}'.`,
    },
    {
      id: `${spec.id}-q3`,
      type: "sentence_order",
      prompt: `Sắp xếp các từ để tạo câu: ${spec.vietnamese}`,
      shuffledWords: makeWordChips(spec.english).reverse(),
      correctSentence: spec.english,
      explanation: `Thứ tự đúng là '${spec.english}'.`,
    },
    {
      id: `${spec.id}-q4`,
      type: "listening",
      prompt: "Nghe câu tiếng Anh B1 và chọn nghĩa đúng.",
      audioText: spec.english,
      answerMode: "multiple_choice",
      options: [
        { id: "a", label: spec.listeningOptions[0] },
        { id: "b", label: spec.listeningOptions[1] },
        { id: "c", label: spec.listeningOptions[2] },
      ],
      correctOptionId: spec.correctListeningOptionId,
      explanation: `Bạn vừa nghe '${spec.english}', nghĩa là '${spec.vietnamese}'.`,
    },
  ];
}

export const mockLessons: LessonSession[] = lessonSpecs.map((spec) => ({
  id: spec.id,
  title: spec.title,
  description: spec.description,
  xpPerQuestion: spec.xpPerQuestion,
  questions: buildQuestions(spec),
}));

export function getMockLesson(lessonId: string) {
  return mockLessons.find((lesson) => lesson.id === lessonId);
}
